export function add_two(num){
    return num + 2;
}

export function add(num1, num2) {
   return num1 + num2;
}

export function subtract_two(num){
    return num - 2;
}

export function subtract(num1, num2) {
    return num1 - num2;
}

export function multiply_two(num) {
    return num * 2;
}

export function multiply(num1, num2) {
    return num1 * num2;
}